package com.serenitydojo;

public class Cat {
    private String name;
    private String food;
    private int age;

    public Cat(String name, String food, int age) {
        this.name = name;
        this.food = food;
        this.age = age;
    }

    public String getName() {
        return name;
    }

    public String getFood() {
        return food;
    }

    public int getAge() {
        return age;
    }


}
